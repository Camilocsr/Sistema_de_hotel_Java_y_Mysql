    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        if(txtproducto.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Es hobligatorio seleccionar un producto.");
            btnbuscarproducto.requestFocus();
            return;
        }
        if(txtcantidad.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Ingrese una cantidad de productos.");
            txtcantidad.requestFocus();
            return;
        }
        if(txtprecio_venta.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Ingrese el precio de venta del producto.");
            txtprecio_venta.requestFocus();
            return;
        }

        vconsumo dts = new vconsumo();
        fconsumo func = new fconsumo();

        dts.setIdreserva(Integer.parseInt(txtidreserva.getText()));
        dts.setIdproducto(Integer.parseInt(txtidproducto.getText()));
        dts.setCantidad(Double.parseDouble(txtcantidad.getText()));
        dts.setPrecio_venta(Double.parseDouble(txtprecio_venta.getText()));


        int seleccionado = cboestado.getSelectedIndex();
        dts.setEstado((String)cboestado.getItemAt(seleccionado));

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showConfirmDialog(rootPane, "El Consumo " + txtproducto.getText() + " del cliente. "
                        + txtcliente.getText() + " fue Guardado Correctamente");
                mostrar(idreserva);
                inhabilitar();
            }
        } else if (accion.equals("editar")) {
            dts.setIdconsumo(Integer.parseInt(txtidconsumo.getText()));
            if(func.editar(dts)) {
                JOptionPane.showConfirmDialog(rootPane, "El consumo del cliente " + txtcliente.getText() + " fue editado correctamente");
                mostrar(idreserva);
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

